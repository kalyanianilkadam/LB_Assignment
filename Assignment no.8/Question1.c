void Number(int iNo)
{
   if(iNo <= 50 )
   {
    printf("Smaller");
   }
   else if(iNo>=50 && iNo<=100)
   {
    printf("medium");
   }
   else
   {
    printf("Greater");
   }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter number:");
    scanf("%d",&iValue);

    Number(iValue);

    return 0;
}


