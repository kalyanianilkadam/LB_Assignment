void Table(int iNo)
{
   int iCnt = 0;
   int iAns = 0;

   for(iCnt = 10 ; iCnt >=1; iCnt--)
   {
       iAns = iNo * iCnt;
       printf("%d\t", iAns);
   }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter number:");
    scanf("%d",&iValue);

    Table(iValue);

    return 0;
}



