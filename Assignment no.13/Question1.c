#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_numbers
//  Description :   It is used to display numbers
//  Input :         int 
//  Output :        int 
//  Author :        Kalyani Anil Kadam.
//  Date :          27/10/2025
//
