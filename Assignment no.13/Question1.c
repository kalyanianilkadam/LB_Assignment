void print_numbers(int limit)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <=limit; iCnt++)
    {
        printf("%d\t",iCnt);
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;

    printf("Enter Number:");
    scanf("%d",&limit);
    print_numbers(limit);
    return 0;
}
