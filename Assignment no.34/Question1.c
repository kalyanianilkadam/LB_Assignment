#include <stdio.h>

/////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayASCII
//  Description :   It is used to display ASCII table
//  Input :         char
//  Output :        char
//  Author :        Kalyani Anil Kadam.
//  Date :          16/12/2025
//
//////////////////////////////////////////////////////////////////////////////

void DisplayASCII()
{
    printf("ASCII Table (0 to 255):\n");
    printf("Dec\tHex\tOct\tChar\n");

    for (int i = 0; i <= 255; i++)
    {
        printf("%d\t%X\t%o\t%c\n", i, i, i, (char)i);
    }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    DisplayASCII();
    return 0;
}