#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is used to display decimal, octal,hexadecimal character
//  Input :         char
//  Output :        char
//  Author :        Kalyani Anil Kadam.
//  Date :          16/12/2025
//
//////////////////////////////////////////////////////////////////////////////

void Display(char ch)
{
    printf("Decimal : %d\n", ch);
    printf("Octal   : %o\n", ch);
    printf("Hexadecimal : 0X%x\n", ch);
}

///////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';
    
    printf("Enter the character : ");
    scanf("%c",&cValue);
    
    Display(cValue);
    
    return 0;
}