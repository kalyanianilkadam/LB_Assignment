void MultipleDisplay(int iNo)
{
   int iCnt = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
            printf("%d\t ", iNo * iCnt);
        
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    printf("Enter number:");
    scanf("%d",&iValue);

    MultipleDisplay(iValue);

    return 0;
}


