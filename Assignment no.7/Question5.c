///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultipleDisplay
//  Description :   It is used to display multiplicatiom
//  Input :         int
//  Output :        int
//  Author :        Kalyani Anil Kadam.
//  Date :          27/10/2025
