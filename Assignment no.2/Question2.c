#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   It is used to Display *
//  Input :         int
//  Output :        int
//  Author :        Kalyani Anil Kadam.
//  Date :          26/10/2025
//
/////////////////////////////////////////////////////////////////////////////////


void Display(int iNo)
{
    if(iNo < 0)
    {
         iNo= -iNo;
    }
   
    while(iNo > 0)
    {
        printf("*");
        iNo--;
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0;

    printf("Enter Number:");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}