void Display(int iNo)
{
    int iCnt = 0;
    
    if(iNo < 0)
    {
         iNo= -iNo;
    }
   
    
    while(iCnt < iNo)
    {
        printf("*");
        iCnt++;
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue = 0;

    printf("Enter Number:");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}