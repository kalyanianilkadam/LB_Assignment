#include <stdio.h>


////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Sum
//  Description   : It is used to  display addition of digits
//  Input         : int
//  OutPut        : int
//  Author        : Kalyani Anil Kadam
//  Date          : 18/12/2025
//      
////////////////////////////////////////////////////////////////////////////////

int Sum(int iNo)
{
    if (iNo == 0)
    {
        return 0;
    }
    return (iNo % 10) + Sum(iNo / 10);
}

///////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;
    
    printf("Enter number: ");
    scanf("%d", &iValue);
    
    iRet = Sum(iValue);
    
    printf("%d", iRet);
    
    return 0;
}