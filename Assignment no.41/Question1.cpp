#include <iostream>
using namespace std;

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : It is used to  display number and *
//  Input         : pattern 
//  OutPut        : pattern 
//  Author        : Kalyani Anil Kadam
//  Date          : 18/12/2025
//      
////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    if (iNo > 0)
    {
        cout << iNo << " * ";
        Display(iNo - 1);
    }
}

///////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    
    cout << "Enter number : ";
    cin >> iValue;
    
    Display(iValue);
    
    return 0;
}

