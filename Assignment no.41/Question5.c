#include <stdio.h>


////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Mult
//  Description   : It is used to  display multiplication of digits
//  Input         : int
//  OutPut        : int
//  Author        : Kalyani Anil Kadam
//  Date          : 18/12/2025
//      
////////////////////////////////////////////////////////////////////////////////

int Mult(int iNo)
{
    if (iNo == 0)
        return 1;
    return (iNo % 10) * Mult(iNo / 10);
}

///////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;
    
    printf("Enter number: ");
    scanf("%d", &iValue);
    
    iRet = Mult(iValue);
    
    printf("%d", iRet);
    
    return 0;
}