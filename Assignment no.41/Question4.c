#include <stdio.h>


////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Fact
//  Description   : It is used to  display factorial
//  Input         : int
//  OutPut        : int
//  Author        : Kalyani Anil Kadam
//  Date          : 18/12/2025
//      
////////////////////////////////////////////////////////////////////////////////

int Fact(int iNo)
{
    if (iNo == 0 || iNo == 1)
        return 1;
    else
        return iNo * Fact(iNo - 1);
}

///////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application 
// 
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter number: ");
    scanf("%d", &iValue);

    iRet = Fact(iValue);

    printf("%d", iRet);

    return 0;
}