void CheckLeapYear(int year)
{
    if((year % 4 == 0) )
        {
              printf("Leap year");
        }
    else
        {
             printf("Not a leap year");
        }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int yr;

    printf("Enter year:");
    scanf("%d",&yr);

    CheckLeapYear(yr);

    return 0;
}