///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CheckLeapYear
//  Description :   It is used to display leap year
//  Input :         int 
//  Output :        int
//  Author :        Kalyani Anil Kadam.
//  Date :          26/10/2025
//
