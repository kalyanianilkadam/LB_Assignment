void CheckNumberType(int num)
{
    if(num >0)
    {
        printf("It Is Positive Number");
    }
    else if(num < 0)
    {
        printf("It Is negtive Number");
    }
    else
    {
        printf("It Is Zero Number");
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number;

    printf("Enter Number:");
    scanf("%d",&number);

    CheckNumberType(number);

    return 0;
}