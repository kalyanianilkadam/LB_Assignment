void PrintEven(int iNo)
{
    if(iNo <= 0)
    {
        return ;
    }
    int iCnt = 0;
    int iEvenNumber = 2;

    for(iCnt = 0; iCnt< iNo; iCnt++)
    {
        printf("%d\t",iEvenNumber);
        iEvenNumber +=2;
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;

    printf("Enter Number:\n");
    scanf("%d",&iValue);

    PrintEven(iValue);

    return 0;
}