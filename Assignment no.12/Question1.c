#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Print_factors
//  Description :   It is used to display printing factors
//  Input :         int 
//  Output :        int 
//  Author :        Kalyani Anil Kadam.
//  Date :          27/10/2025
//
//////////////////////////////////////////////////////////////////////////////

void print_factors(int number)
{
    int i = 0;
    printf("Factors of %d are: ", number);
    for (i = 1; i <= number; i++)
    {
        if (number % i == 0)
        {
            printf("%d ", i);
        }
    }
    printf("\n");
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////


int main()
{
    int number;
    printf("Enter number: ");
    scanf("%d", &number);

    print_factors(number);

    return 0;
}
