#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : sum_of_factors
//  Description :   It is used to display Sum of all factors
//  Input :         int 
//  Output :        int 
//  Author :        Kalyani Anil Kadam.
//  Date :          27/10/2025
//
//////////////////////////////////////////////////////////////////////////////

int sum_of_factors(int number)
{
    int sum = 0;
    for (int i = 1; i <= number; i++)
    {
        if (number % i == 0)
        {
            sum += i;
        }
    }
    return sum;
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////


int main(void)
{
    int number;
    printf("Enter number: ");
    scanf("%d", &number);

    printf("Sum of factors of %d is: %d\n", number, sum_of_factors(number));

    return 0;
}
