#include<stdio.h>
#include<stdbool.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : is_divisible_by_five
//  Description :   It is used to display number is didvisible by 5 or not
//  Input :         int 
//  Output :        bool
//  Author :        Kalyani Anil Kadam.
//  Date :          27/10/2025
//
//////////////////////////////////////////////////////////////////////////////

bool is_divisible_by_five(int number)
{
    if(number %5 == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number = 0;

    printf("Enter Number:");
    scanf("%d",&number);

    printf("%s\n", is_divisible_by_five(number) ? "Yes" : "No");


    return 0;
}