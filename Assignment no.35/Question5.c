#include <stdio.h>
#include <string.h>

/////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Reverse
//  Description :   It is used to display string in reverse order
//  Input :         char
//  Output :        char
//  Author :        Kalyani Anil Kadam.
//  Date :          16/12/2025
//
//////////////////////////////////////////////////////////////////////////////

void Reverse(char *str)
 {
    int length = strlen(str);
    int i;

    printf("Output : \"");

    for (i = length - 1; i >= 0; i--) 
    {
        printf("%c", str[i]);
    }
    printf("\"\n");
}

///////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main() 
{
    char arr[20];
    printf("Enter string : ");
    scanf("%[^\n]s", arr);  
    Reverse(arr);
    return 0;
}