void FactRev(int iNo)
{
    int iCnt = 0;

    for(iCnt = iNo; iCnt >= 1; iCnt--)
    {
        if((iNo %iCnt) == 0 )
        {
            printf("%d",iCnt);
        }
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number:");
    scanf("%d",&iValue);

     FactRev(iValue);

    return 0;
}