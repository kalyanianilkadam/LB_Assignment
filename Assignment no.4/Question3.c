void NonFact(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt< iNo; iCnt++)
    {
        if((iNo % iCnt)!= 0)
        {
            printf("%d\t",iCnt);
        }
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter Number:");
    scanf("%d",&iValue);

    NonFact(iValue);

    return 0;
}
