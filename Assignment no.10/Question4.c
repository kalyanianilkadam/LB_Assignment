////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Description :   It is used to convert Fahreheit to celsis
//  Input :         int, int
//  Output :        int
//  Author :        Kalyani Anil Kadam.
//  Date :          26/10/2025
//
/////////////////////////////////////////////////////////////////////////////////

double FhtoCs(float fTemp)
{
    double dCel;
    dCel = ((fTemp-32)*(5.0/9.0
    ));
    return dCel;

}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("enter temperature in Fahreheit:");
    scanf("%f", &fValue);

    dRet = FhtoCs(fValue);

    printf("Temperature is:%f",dRet);
}