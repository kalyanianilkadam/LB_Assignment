#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SquareMeter
//  Description :   It is used to convert squarefeet to area of square
//  Input :         float, float
//  Output :        double
//  Author :        Kalyani Anil Kadam.
//  Date :          26/10/2025
//
/////////////////////////////////////////////////////////////////////////////////


double SquareMeter(int iValue)
{
    double dSMeter;
    dSMeter = iValue * 0.0929; 
    return dSMeter;
}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet: ");
    scanf("%d", &iValue); 

    dRet = SquareMeter(iValue);

    printf("area of square is:%f\n", dRet);
    return 0;
}
