#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : KMtoMeter
//  Description :   It is used to convert Kilometerto meter
//  Input :         int
//  Output :        int
//  Author :        Kalyani Anil Kadam.
//  Date :          26/10/2025
//
/////////////////////////////////////////////////////////////////////////////////

int KMtoMeter(int iNo)
{
    int iDist = 0;
    iDist = iNo * 1000;
    return iDist;

}

/////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function For the Application 
//
/////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0,  iRet = 0;

    printf("enter Distance:");
    scanf("%d", &iValue);

    iRet = KMtoMeter(iValue);

    printf("Distance is:%d",iRet);
}
